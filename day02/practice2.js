//숫자 자료
console.log(2000); // 제품 가격
console.log(4000); // 총 결제 금액
console.log(3); // 주문수량

//문자열 자료
console.log("아메리카노"); // 제품명
console.log("커피"); // 카테고리명
console.log("A-03"); // 주문번호

//불리언 자료
console.log(false); // 품절
console.log(true); // 영수증 출력 여부
console.log(true); // 적립 여부

//배열 자료
console.log(["커피","음료","주스/에이드"]); // 카테고리명들
console.log(["아메리카노","빽사이즈아메리카노(ICED)","원조커피"]); // 제품명들
